# 运算符
# 分类:算术运算符，比较（关系）运算符，逻辑运算符，赋值运算符，成员和身份运算符，位和位移运算符

# 1.算术运算符
print(5 / 2)  # 会除尽
print(5 // 2)  # 整除
print(5 % 2)  # 求余数
print(5 ** 2)  # 求5的2次幂

# 2.比较运算符：两边运算的是数值，得到是布尔值
print(3 == 5)
print(3 != 5)
